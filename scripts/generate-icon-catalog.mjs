import fs from 'node:fs';
import path from 'node:path';

const rootDir = process.cwd();
const iconsDir = path.join(rootDir, 'assets', 'icons');
const outputPath = path.join(rootDir, 'components', 'Icons', 'icon-catalog.ts');

function uniqueSorted(values) {
  return Array.from(new Set(values)).sort((a, b) => a.localeCompare(b));
}

if (!fs.existsSync(iconsDir)) {
  throw new Error(`Icons directory not found: ${iconsDir}`);
}

const entries = fs
  .readdirSync(iconsDir, { withFileTypes: true })
  .filter((entry) => entry.isFile() && entry.name.endsWith('.svg'))
  .map((entry) => entry.name);

const lineNames = [];
const fillNames = [];
const plainNames = [];
const allNames = [];

for (const fileName of entries) {
  const rawName = fileName.replace(/\.svg$/i, '');
  allNames.push(rawName);

  if (rawName.endsWith('-line')) {
    lineNames.push(rawName.slice(0, -'-line'.length));
    continue;
  }

  if (rawName.endsWith('-fill')) {
    fillNames.push(rawName.slice(0, -'-fill'.length));
    continue;
  }

  plainNames.push(rawName);
}

const catalog = {
  lineNames: uniqueSorted(lineNames),
  fillNames: uniqueSorted(fillNames),
  plainNames: uniqueSorted(plainNames),
  allNames: uniqueSorted(allNames),
};

const text = `/* Auto-generated by scripts/generate-icon-catalog.mjs */\n\nexport const iconCatalog = ${JSON.stringify(catalog, null, 2)} as const;\n`;

fs.writeFileSync(outputPath, text, 'utf8');

console.log(
  `Generated icon catalog (${catalog.allNames.length} files, ${catalog.lineNames.length} line names, ${catalog.fillNames.length} fill names, ${catalog.plainNames.length} plain names) -> ${path.relative(rootDir, outputPath)}`,
);
