import fs from 'node:fs';
import path from 'node:path';

const rootDir = process.cwd();
const componentsDir = path.join(rootDir, 'components');
const outputPath = path.join(rootDir, 'component-explorer-metadata.ts');

function readIfExists(filePath) {
  if (!fs.existsSync(filePath)) {
    return '';
  }

  return fs.readFileSync(filePath, 'utf8');
}

function uniqueSorted(values, numeric = false) {
  const unique = Array.from(new Set(values));

  if (numeric) {
    return unique.sort((a, b) => Number(a) - Number(b));
  }

  return unique.sort((a, b) => String(a).localeCompare(String(b)));
}

function collectMatches(text, regex, extractor = (match) => match[1]) {
  const results = [];
  let match = regex.exec(text);

  while (match) {
    const value = extractor(match);

    if (value !== undefined && value !== null && value !== '') {
      results.push(value);
    }

    match = regex.exec(text);
  }

  return results;
}

function extractMetadata(folderName) {
  const baseDir = path.join(componentsDir, folderName);
  const docsPath = path.join(baseDir, `${folderName}.docs.md`);
  const sourcePath = path.join(baseDir, `${folderName}.tsx`);
  const previewPath = path.join(baseDir, `${folderName}.preview.tsx`);

  const docs = readIfExists(docsPath);
  const source = readIfExists(sourcePath);
  const preview = readIfExists(previewPath);
  const combinedSource = `${source}\n${preview}`;

  const docTokenCandidates = collectMatches(docs, /`([^`]+)`/g, (m) => m[1].trim());
  const tokens = uniqueSorted(
    docTokenCandidates.filter((token) => /^(colors|typography|spacing|radius|border|shadows|zIndex)\./.test(token)),
  );

  const spacingScale = uniqueSorted(
    collectMatches(combinedSource, /spacing\.scale\[['"](\d+)['"]\]/g, (m) => Number(m[1])),
    true,
  );

  const spacingPrimitive = uniqueSorted(
    collectMatches(combinedSource, /spacing\.primitive\[['"](\d+)['"]\]/g, (m) => Number(m[1])),
    true,
  );

  const borderWidths = uniqueSorted(
    collectMatches(combinedSource, /border\.width\[['"](\d+)['"]\]/g, (m) => Number(m[1])),
    true,
  );

  const radiusScale = uniqueSorted(
    collectMatches(combinedSource, /radius\.scale(?:\.([A-Za-z0-9_]+)|\[['"]([^'"]+)['"]\])/g, (m) => m[1] ?? m[2]),
  );

  const typographyScale = uniqueSorted(
    collectMatches(combinedSource, /typography\.scale\.([A-Za-z0-9_]+)\.([A-Za-z0-9_]+)/g, (m) => `${m[1]}.${m[2]}`),
  );

  return {
    id: folderName,
    docsPath: path.relative(rootDir, docsPath),
    sourcePath: path.relative(rootDir, sourcePath),
    tokens,
    styleNumbers: {
      spacingScale,
      spacingPrimitive,
      borderWidths,
      radiusScale,
      typographyScale,
    },
  };
}

const folders = fs
  .readdirSync(componentsDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort((a, b) => a.localeCompare(b));

const metadata = {};

for (const folder of folders) {
  metadata[folder] = extractMetadata(folder);
}

const output = `/* Auto-generated by scripts/generate-component-metadata.mjs */\n\nexport const componentMetadata = ${JSON.stringify(metadata, null, 2)} as const;\n`;

fs.writeFileSync(outputPath, output, 'utf8');
console.log(`Generated metadata for ${folders.length} components -> ${path.relative(rootDir, outputPath)}`);
